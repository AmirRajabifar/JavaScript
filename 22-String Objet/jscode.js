var hello = " Hello, how are you doing? ";


document.write(hello);

hello = hello.toUpperCase();
document.write(hello);
hello = hello.toLowerCase();
document.write(hello);
hello = hello.replace("doing", "today");
document.write(hello);
hello = hello.bold();
document.write(hello);
hello = hello.italics();
document.write(hello);
hello = hello.length;
document.write(hello);
hello = hello.charAt(4);
document.write(hello);
